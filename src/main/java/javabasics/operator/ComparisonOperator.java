package javabasics.operator;

/**
 * 比较运算符
 */
public class ComparisonOperator {
    public static void main(String[] args) {
        //比较运算符的结果都是boolean型
        //相等于
        System.out.println(4 == 3);
        //不等于
        System.out.println(4 != 3);
        //小于
        System.out.println(4 < 3);
        //大于
        System.out.println(4 > 3);
        //小于等于
        System.out.println(4 <= 3);
        //大于等于
        System.out.println(4 >= 3);
    }
}
